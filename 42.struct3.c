#include <stdio.h>
#include <math.h>

// 두 점 사이의 거리를 구하는 프로그램입니다. 
struct point{
	int x;
	int y;
}; 

int main(void){
	struct point p1, p2;
	int xDiff, yDiff; // xDiff : x좌표의 차이점 , yDiff : y좌표의 차이점.
	double distance;
	
	printf("1번 점의 좌표를 입력하세요 : ");
	scanf("%d %d", &p1.x, &p1.y);
	
	printf("2번 점의 좌표를 입력하세요 : ");
	scanf("%d %d", &p2.x, &p2.y);
	
	xDiff = p1.x - p2.x;
	yDiff = p1.y - p2.y;
	
	distance = sqrt(xDiff * xDiff + yDiff * yDiff); // sqrt : 제곱근을 구하는 함수. 
	printf("두 점 사이의 거리는 %f입니다.\n", distance);
	return 0;
}
